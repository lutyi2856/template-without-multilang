import React from "react";
import { Text } from "@/components/design-system";

interface StarRatingProps {
  rating: number; // 0-5
  className?: string;
}

/**
 * Визуальный рейтинг со звездами (SVG с частичным заполнением)
 *
 * DESIGN:
 * - SVG звезды из Figma
 * - Размер: 26x23px
 * - Цвет: #526AC2 (unident-primary)
 * - Частичное заполнение через clipPath
 *
 * EXAMPLE:
 * rating: 4.7 → 4 полных звезды + 0.7 последней (0.3 скрыто)
 * rating: 5.0 → 5 полных звезд
 * rating: 3.5 → 3 полных звезды + 0.5 четвертой
 */
export function StarRating({ rating, className = "" }: StarRatingProps) {
  // Уникальный ID для clipPath (чтобы избежать конфликтов между несколькими компонентами)
  const componentId = React.useMemo(
    () => Math.random().toString(36).substr(2, 9),
    []
  );

  // Вычисление заполнения для каждой звезды (0-1)
  const stars = Array.from({ length: 5 }, (_, index) => {
    const starValue = rating - index;
    if (starValue >= 1) return 1; // Полная звезда
    if (starValue > 0) return starValue; // Частичная (0-1)
    return 0; // Пустая
  });

  // SVG звезды из Figma
  const StarSVG = ({
    fillPercent,
    index,
  }: {
    fillPercent: number;
    index: number;
  }) => {
    const clipPathId = `star-clip-${componentId}-${index}`;
    const width = 26;
    const height = 23;
    const clipWidth = width * fillPercent;

    return (
      <svg
        width={width}
        height={height}
        viewBox="0 0 26 23"
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
        className="flex-shrink-0"
      >
        {fillPercent > 0 && fillPercent < 1 ? (
          <>
            {/* Пустая звезда (фон) */}
            <path
              d="M12.4142 0.353892C12.5806 -0.117928 13.2478 -0.117928 13.4142 0.353893L16.1247 8.03961C16.1994 8.25166 16.3998 8.39348 16.6247 8.39348H25.2973C25.8214 8.39348 26.0278 9.07275 25.5923 9.36428L18.6527 14.0096C18.4515 14.1443 18.3671 14.3982 18.4476 14.6265L21.1155 22.1918C21.2851 22.6727 20.7444 23.0924 20.3206 22.8087L13.2092 18.0483C13.0307 17.9289 12.7978 17.9289 12.6193 18.0483L5.50785 22.8087C5.08406 23.0924 4.54329 22.6727 4.7129 22.1918L7.38084 14.6265C7.46137 14.3982 7.37697 14.1443 7.17575 14.0096L0.236169 9.36427C-0.199332 9.07275 0.00703692 8.39348 0.531105 8.39348H9.20375C9.4286 8.39348 9.62899 8.25166 9.70377 8.03961L12.4142 0.353892Z"
              fill="#526AC2"
              fillOpacity="0.2"
            />
            {/* Частично заполненная звезда */}
            <defs>
              <clipPath id={clipPathId}>
                <rect x="0" y="0" width={clipWidth} height={height} />
              </clipPath>
            </defs>
            <g clipPath={`url(#${clipPathId})`}>
              <path
                d="M12.4142 0.353892C12.5806 -0.117928 13.2478 -0.117928 13.4142 0.353893L16.1247 8.03961C16.1994 8.25166 16.3998 8.39348 16.6247 8.39348H25.2973C25.8214 8.39348 26.0278 9.07275 25.5923 9.36428L18.6527 14.0096C18.4515 14.1443 18.3671 14.3982 18.4476 14.6265L21.1155 22.1918C21.2851 22.6727 20.7444 23.0924 20.3206 22.8087L13.2092 18.0483C13.0307 17.9289 12.7978 17.9289 12.6193 18.0483L5.50785 22.8087C5.08406 23.0924 4.54329 22.6727 4.7129 22.1918L7.38084 14.6265C7.46137 14.3982 7.37697 14.1443 7.17575 14.0096L0.236169 9.36427C-0.199332 9.07275 0.00703692 8.39348 0.531105 8.39348H9.20375C9.4286 8.39348 9.62899 8.25166 9.70377 8.03961L12.4142 0.353892Z"
                fill="#526AC2"
              />
            </g>
          </>
        ) : fillPercent === 1 ? (
          // Полная звезда
          <path
            d="M12.4142 0.353892C12.5806 -0.117928 13.2478 -0.117928 13.4142 0.353893L16.1247 8.03961C16.1994 8.25166 16.3998 8.39348 16.6247 8.39348H25.2973C25.8214 8.39348 26.0278 9.07275 25.5923 9.36428L18.6527 14.0096C18.4515 14.1443 18.3671 14.3982 18.4476 14.6265L21.1155 22.1918C21.2851 22.6727 20.7444 23.0924 20.3206 22.8087L13.2092 18.0483C13.0307 17.9289 12.7978 17.9289 12.6193 18.0483L5.50785 22.8087C5.08406 23.0924 4.54329 22.6727 4.7129 22.1918L7.38084 14.6265C7.46137 14.3982 7.37697 14.1443 7.17575 14.0096L0.236169 9.36427C-0.199332 9.07275 0.00703692 8.39348 0.531105 8.39348H9.20375C9.4286 8.39348 9.62899 8.25166 9.70377 8.03961L12.4142 0.353892Z"
            fill="#526AC2"
          />
        ) : (
          // Пустая звезда
          <path
            d="M12.4142 0.353892C12.5806 -0.117928 13.2478 -0.117928 13.4142 0.353893L16.1247 8.03961C16.1994 8.25166 16.3998 8.39348 16.6247 8.39348H25.2973C25.8214 8.39348 26.0278 9.07275 25.5923 9.36428L18.6527 14.0096C18.4515 14.1443 18.3671 14.3982 18.4476 14.6265L21.1155 22.1918C21.2851 22.6727 20.7444 23.0924 20.3206 22.8087L13.2092 18.0483C13.0307 17.9289 12.7978 17.9289 12.6193 18.0483L5.50785 22.8087C5.08406 23.0924 4.54329 22.6727 4.7129 22.1918L7.38084 14.6265C7.46137 14.3982 7.37697 14.1443 7.17575 14.0096L0.236169 9.36427C-0.199332 9.07275 0.00703692 8.39348 0.531105 8.39348H9.20375C9.4286 8.39348 9.62899 8.25166 9.70377 8.03961L12.4142 0.353892Z"
            fill="#526AC2"
            fillOpacity="0.2"
          />
        )}
      </svg>
    );
  };

  return (
    <div className={`flex items-center gap-2 ${className}`}>
      {/* Визуальные звезды */}
      <div className="flex items-center gap-0.5">
        {stars.map((fillPercent, index) => (
          <StarSVG key={index} fillPercent={fillPercent} index={index} />
        ))}
      </div>

      {/* Числовой рейтинг */}
      <Text
        variant="review-answer"
        className="text-unident-primary font-medium"
        as="span"
      >
        {rating.toFixed(1)}
      </Text>
    </div>
  );
}
